using System;

namespace PongGamee
{
	public static class Calculator
	{
		public static int OpTellen (int a, int b)
		{
			int c = a + b;
			return c;
		}
		public static int Aftrekken (int d, int e)
		{
			int f = d - e;
			return f;
		}
		public static int Delen (int g, int h)
		{
			int i = g / h;
			return i;
		}
		public static int Vermenigvuldigen (int j, int k)
		{
			int l = j * k;
			return l;
		}


	
	}
}

